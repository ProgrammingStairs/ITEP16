public class Operation {
        public int getSum(int a,int b){
                return a+b;
        } 
        public int getSub(int a,int b){
                return a-b;
        } 
        public int getMul(int a,int b){
                return a*b;
        } 
        public double getDiv(double a,double b){
                return a/b;
        } 
        public int getMod(int a,int b){
                return a%b;
        } 
}
